import type { Chain } from './Chain';
import type { Fee } from './Fee';
import type { FillStatus } from './FillStatus';
import type { Item } from './Item';
import type { MetadataFieldFilter } from './MetadataFieldFilter';
import type { OrderStatus } from './OrderStatus';
import type { ProtocolData } from './ProtocolData';
import type { TraitFilter } from './TraitFilter';

export type Order = {
  account_address: string;
  buy: Array<Item>;
  fees: Array<Fee>;
  chain: Chain;
  /**
   * Time the Order is created
   */
  created_at: string;
  /**
   * Time after which the Order is considered expired
   */
  end_at: string;
  /**
   * Global Order identifier
   */
  id: string;
  order_hash: string;
  protocol_data: ProtocolData;
  /**
   * A random value added to the create Order request
   */
  salt: string;
  sell: Array<Item>;
  /**
   * Digital signature generated by the user for the specific Order
   */
  signature: string;
  /**
   * Time after which Order is considered active
   */
  start_at: string;
  status: OrderStatus;
  /**
   * Order type
   */
  type: Order.type;
  /**
   * Time the Order is last updated
   */
  updated_at: string;
  fill_status: FillStatus;
  /**
   * Trait criteria for trait bids when returned by the API
   */
  trait_criteria?: Array<TraitFilter>;
  /**
   * Metadata ID (stack ID) for metadata bids created with a single metadata
   * stack. Omitted for metadata bids created with `metadata_criteria`.
   */
  metadata_id?: string;
  /**
   * Field-level metadata filters for metadata bids created with criteria
   * instead of a `metadata_id`. Omitted for metadata bids created with a
   * `metadata_id`.
   */
  metadata_criteria?: Array<MetadataFieldFilter>;
};

export namespace Order {

  /**
   * Order type
   */
  export enum type {
    LISTING = 'LISTING',
    BID = 'BID',
    COLLECTION_BID = 'COLLECTION_BID',
    TRAIT_BID = 'TRAIT_BID',
    METADATA_BID = 'METADATA_BID',
  }


}

